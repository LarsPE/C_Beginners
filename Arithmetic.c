#include<stdio.h>

int main(int argc, char const *argv[]) {
  // arithmetic operators

  // + addition
  // - subtraction
  // * multiplication
  // / division
  // % modulus
  // ++ increment
  // -- decrement

  int x = 5;
  int y = 2;

  printf("%d\n", x+y);
  printf("%d\n", x-y);
  printf("%d\n", x*y);
  printf("%.2f\n", x/(float)y);
  printf("%d\n", x%y);
  printf("%d\n", ++x);
  printf("%d\n", --y);
  return 0;
}
